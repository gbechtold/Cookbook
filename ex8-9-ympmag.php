Beim ersten Briefing konnten 3 Themenstellungen als wichtig identifiziert werden: 

Akustik und Fläche  
Akustik korreliert mit Fläche. Wenn man in einem Raum die Akustische Qualität beeinflussen will ist die veränderbare Fläche eine kritische Größe. 
Stark vereinfacht gesagt ist mehr Akustisch-aktive besser als weniger. 
Zusätzlich steigt die akustische Qualität mit der Dicke akustisch aktiven Materials. 
Deshalb ist es kritisch dass Getzner ein attraktiv anmutendes, nachrüstbares Modulsystem mit akustischer Wirksamkeit anbietet. 

Segel für große Räume
Akustiksegel sind in öffentlichen Räumen einfach installierbar. 
Die Anforderungen für es an diese Objekte sind technisch und aus Nutzersicht abzustecken und zu definieren. 
Akustische Wirksamkeit, funktionale Anwendung und passende Anmutung sind dabei entscheidend.

